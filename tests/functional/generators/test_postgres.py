import difflib
import textwrap
from pathlib import Path

from qsdl.core import generate
from qsdl.generators.postgres.config import Config


def wrapper_generate(test_input: str) -> str:
    """Generates and returns the OpenAPI spec as dict.

    Args:
        test_input (str): The QSDL definition.

    Returns:
        dict: The OpenAPI specification as dict.
    """
    test_input = textwrap.dedent(test_input)
    test_output = Path("srcgen/" + "/")

    # generate
    assert generate(test_output, generator_name="postgres", raw_schema=test_input) is None

    output_file = Path("srcgen/" + Config.file_name)

    assert output_file.is_file()

    with open(output_file, encoding="utf-8") as file:
        schema = file.read()

    return schema


def assert_schema(schema: str, expected_schema: str) -> None:
    schema_lines = [line.strip() for line in schema.splitlines() if line.strip() and not line.startswith("--")]
    expected_lines = [line.strip() for line in expected_schema.splitlines() if line.strip()]

    if schema_lines != expected_lines:
        diff = difflib.unified_diff(expected_lines, schema_lines, fromfile="Expected", tofile="Generated", lineterm="")
        diff = "\n".join(diff)
        raise AssertionError(f"Schema mismatch:\n{diff}")


class TestGeneratorPostgres:
    """Test postgres generator"""

    def test_basic_types(self) -> None:
        # Given
        test_input = """\
          base A {
            int: Int
            long: Long
          }

          base B extends A {
            float: Float
            double: Double
            string: String
          }

          type Foo extends B {
            boolean: Boolean
            date: Date
            datetime: Datetime
            object: Object
          }
        """

        # When
        schema = wrapper_generate(test_input)

        # Then
        expected_schema = """\
          create table if not exists T_FOO (
            id BIGINT generated by default as identity primary key,
            uid VARCHAR unique,
            iv INTEGER,
            int INTEGER,
            long BIGINT,
            float REAL,
            double DOUBLE PRECISION,
            string TEXT,
            boolean BOOLEAN,
            date DATE,
            datetime TIMESTAMP,
            object JSONB
          );
        """

        assert_schema(schema, expected_schema)

    def test_basic_types_as_list(self) -> None:
        # Given
        test_input = """\
          type Foo {
            int: [Int]
            long: [Long]
            float: [Float]
            double: [Double]
            string: [String]
            boolean: [Boolean]
            date: [Date]
            datetime: [Datetime]
            object: [Object]
          }
        """

        # When
        schema = wrapper_generate(test_input)

        # Then
        expected_schema = """\
          create table if not exists T_FOO (
            id BIGINT generated by default as identity primary key,
            uid VARCHAR unique,
            iv INTEGER,
            int INTEGER [],
            long BIGINT [],
            float REAL [],
            double DOUBLE PRECISION [],
            string TEXT [],
            boolean BOOLEAN [],
            date DATE [],
            datetime TIMESTAMP [],
            object JSONB []
          );
        """

        assert_schema(schema, expected_schema)

    def test_enum_usage(self) -> None:
        # Given
        test_input = """\
          enum Status {
            OPEN
            TO_DO
            CLOSED
          }

          type Foo {
            status: Status
            states: [Status]
          }
        """

        # When
        schema = wrapper_generate(test_input)

        # Then
        expected_schema = """\
          create table if not exists T_FOO (
            id BIGINT generated by default as identity primary key,
            uid VARCHAR unique,
            iv INTEGER,
            status TEXT check (status in ('OPEN', 'TO_DO', 'CLOSED')),
            states TEXT [] check (states is null or array_length(states, 1) = 0 or states <@ ARRAY['OPEN', 'TO_DO', 'CLOSED'])
          );
        """

        assert_schema(schema, expected_schema)

    def test_require_and_unique(self) -> None:
        # Given
        test_input = """\
          type TableFour {
            aaa: String!
            bbb: [String]!
            ccc: String @unique
            ddd: [String] @unique
            eee: String! @unique
            fff: [String]! @unique
          }
        """

        # When
        schema = wrapper_generate(test_input)

        # Then
        expected_schema = """\
          create table if not exists T_TABLE_FOUR (
            id BIGINT generated by default as identity primary key,
            uid VARCHAR unique,
            iv INTEGER,
            aaa TEXT not null,
            bbb TEXT [] not null,
            ccc TEXT unique,
            ddd TEXT [] unique,
            eee TEXT not null unique,
            fff TEXT [] not null unique
          );
        """

        assert_schema(schema, expected_schema)

    def test_one_to_one(self) -> None:
        # Given
        test_inputs = [
            """\
              type User {
                primary_metric: Metric
                secondary_metric: Metric
              }
              base Metric {
                likes: Int
              }
            """,
            """\
              type User {
                primary_metric: Metric
                secondary_metric: Metric
              }
              type Metric {
                likes: Int
              }
            """,
        ]

        expected_schema = """\
          create table if not exists T_USER (
            id BIGINT generated by default as identity primary key,
            uid VARCHAR unique,
            iv INTEGER,
            primary_metric_metric_id BIGINT unique,
            secondary_metric_metric_id BIGINT unique
          );
          create table if not exists T_METRIC (
            id BIGINT generated by default as identity primary key,
            uid VARCHAR unique,
            iv INTEGER,
            likes INTEGER
          );
          alter table if exists T_USER add constraint FK_USER_PRIMARY_METRIC foreign key (primary_metric_metric_id) references T_METRIC(id);
          alter table if exists T_USER add constraint FK_USER_SECONDARY_METRIC foreign key (secondary_metric_metric_id) references T_METRIC(id);
        """

        # When Then
        for test_input in test_inputs:
            schema = wrapper_generate(test_input)
            assert_schema(schema, expected_schema)

    def test_one_to_many(self) -> None:
        # Given
        test_inputs = [
            """\
              type User {
                metric: [Metric]
              }
              base Metric {
                likes: Int
              }
            """,
            """\
              type User {
                metric: [Metric]
              }
              type Metric {
                likes: Int
              }
            """,
            """\
              type User {
                metric: [Metric] @aggregation
              }
              type Metric {
                likes: Int
              }
            """,
            """\
              type User {
                metric: [Metric] @composition
              }
              type Metric {
                likes: Int
              }
            """,
        ]

        expected_schema = """\
          create table if not exists T_USER (
            id BIGINT generated by default as identity primary key,
            uid VARCHAR unique,
            iv INTEGER
          );
          create table if not exists T_USER_METRIC_TO_METRIC (
            user_id BIGINT not null,
            metric_id BIGINT not null,
            primary key (user_id, metric_id)
          );
          create table if not exists T_METRIC (
            id BIGINT generated by default as identity primary key,
            uid VARCHAR unique,
            iv INTEGER,
            likes INTEGER
          );
          alter table if exists T_USER_METRIC_TO_METRIC add constraint FK_USER_METRIC_TO_METRIC_USER foreign key (user_id) references T_USER(id);
          alter table if exists T_USER_METRIC_TO_METRIC add constraint FK_USER_METRIC_TO_METRIC_METRIC foreign key (metric_id) references T_METRIC(id);
        """

        # When Then
        for test_input in test_inputs:
            schema = wrapper_generate(test_input)
            assert_schema(schema, expected_schema)

