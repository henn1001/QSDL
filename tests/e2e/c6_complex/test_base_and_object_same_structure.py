from pathlib import Path

import pytest

from tests.e2e import assert_mvn, assert_postgres
from tests.e2e.base_e2e_test import BaseE2ETest


class TestE2EBaseAndObjectSameStructure(BaseE2ETest):
    """Test E2E for BASE_AND_OBJECT_SAME_STRUCTURE testcase"""

    TESTCASE = """\
      base AddressValue {
        street: String
        city: String
      }
      type AddressEntity {
        street: String
        city: String
      }
      type Person {
        name: String
        homeAddress: AddressValue
        workAddress: AddressEntity
      }
    """

    def test_postgres(self, postgres_schema: str) -> None:
        """asserts generated Postgres schema is correct"""
        expected_schema = """\
            CREATE TABLE IF NOT EXISTS t_address_entity (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              street TEXT,
              city TEXT
            );
            CREATE TABLE IF NOT EXISTS t_person (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              name TEXT,
              home_address_street TEXT,
              home_address_city TEXT,
              work_address_addressentity_id BIGINT
            );
            ALTER TABLE IF EXISTS t_person ADD CONSTRAINT fk_t_person_work_address FOREIGN KEY (work_address_addressentity_id) REFERENCES t_address_entity(id);
        """

        assert_postgres(postgres_schema, expected_schema)

