from pathlib import Path

import pytest

from tests.e2e import assert_mvn, assert_postgres
from tests.e2e.base_e2e_test import BaseE2ETest


class TestE2EComplexEntityGraph(BaseE2ETest):
    """Test E2E for COMPLEX_ENTITY_GRAPH testcase"""

    TESTCASE = """\
      type Customer {
        name: String
        email: String
      }
      type Product {
        name: String
        price: Float
      }
      type OrderItem {
        quantity: Int
        product: Product
      }
      type Order {
        orderNumber: String
        customer: Customer
        items: [OrderItem] @composition
        relatedProducts: [Product] @aggregation
      }
    """

    def test_postgres(self, postgres_schema: str) -> None:
        """asserts generated Postgres schema is correct"""
        expected_schema = """\
            CREATE TABLE IF NOT EXISTS t_customer (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              name TEXT,
              email TEXT
            );

            CREATE TABLE IF NOT EXISTS t_product (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              name TEXT,
              price REAL
            );

            CREATE TABLE IF NOT EXISTS t_order_item (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              quantity INTEGER,
              product_product_id BIGINT,
              items_order_id BIGINT
            );

            CREATE TABLE IF NOT EXISTS t_order (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              order_number TEXT,
              customer_customer_id BIGINT
            );

            CREATE TABLE IF NOT EXISTS t_order_to_t_product (
              order_id BIGINT NOT NULL,
              product_id BIGINT NOT NULL,
              PRIMARY KEY (order_id, product_id)
            );

            ALTER TABLE IF EXISTS t_order_item ADD CONSTRAINT fk_t_order_item_product FOREIGN KEY (product_product_id) REFERENCES t_product(id);
            ALTER TABLE IF EXISTS t_order_item ADD CONSTRAINT fk_t_order_item_items_order FOREIGN KEY (items_order_id) REFERENCES t_order(id);
            ALTER TABLE IF EXISTS t_order ADD CONSTRAINT fk_t_order_customer FOREIGN KEY (customer_customer_id) REFERENCES t_customer(id);
            ALTER TABLE IF EXISTS t_order_to_t_product ADD CONSTRAINT fk_t_order_to_t_product_order FOREIGN KEY (order_id) REFERENCES t_order(id);
            ALTER TABLE IF EXISTS t_order_to_t_product ADD CONSTRAINT fk_t_order_to_t_product_product FOREIGN KEY (product_id) REFERENCES t_product(id);
        """

        assert_postgres(postgres_schema, expected_schema)

