from pathlib import Path

import pytest

from tests.e2e import assert_schema
from tests.e2e.base_e2e_test import BaseE2ETest


class TestE2ETwoParentOneChild(BaseE2ETest):
    """Test E2E for TWO_PARENT_ONE_CHILD testcase"""

    TESTCASE = """\
        type Fruit {
          name: String
        }
        type Foo {
          name: String
          basket: [Fruit] @composition
        }
        type Bar {
          name: String
          basket: [Fruit] @composition
        }
      """

    def test_postgres(self, postgres_schema: str) -> None:
        """asserts generated Postgres schema is correct"""
        expected_schema = """\
        create table if not exists T_FRUIT (
          id BIGINT generated by default as identity primary key,
          uid VARCHAR unique,
          iv INTEGER,
          name TEXT,
          foo_id BIGINT,
          bar_id BIGINT
        );

        create table if not exists T_FOO (
          id BIGINT generated by default as identity primary key,
          uid VARCHAR unique,
          iv INTEGER,
          name TEXT
        );

        create table if not exists T_BAR (
          id BIGINT generated by default as identity primary key,
          uid VARCHAR unique,
          iv INTEGER,
          name TEXT
        );

        alter table if exists T_FRUIT add constraint FK_T_FRUIT_FOO foreign key (foo_id) references T_FOO(id);
        alter table if exists T_FRUIT add constraint FK_T_FRUIT_BAR foreign key (bar_id) references T_BAR(id);
        """

        assert_schema(postgres_schema, expected_schema)

