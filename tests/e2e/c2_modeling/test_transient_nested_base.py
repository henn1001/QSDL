from pathlib import Path

import pytest

from tests.e2e import assert_mvn, assert_postgres
from tests.e2e.base_e2e_test import BaseE2ETest


class TestE2ETransientNestedBase(BaseE2ETest):
    """E2E test for nested Base types with @transient fields"""

    TESTCASE = """\
      base ContactInfo {
        email: String
        phone: String @transient
      }
      base Address {
        street: String
        city: String
        contact: ContactInfo
      }
      type Company {
        name: String
        headquarters: Address
        office: Address @transient
        warehouse: Address @transient @opaque
      }
    """

    def test_postgres(self, postgres_schema: str) -> None:
        """asserts generated Postgres schema excludes transient fields"""
        expected_schema = """\
          CREATE TABLE IF NOT EXISTS t_company (
            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            uid VARCHAR UNIQUE,
            iv INTEGER,
            name TEXT,
            headquarters_street TEXT,
            headquarters_city TEXT,
            headquarters_contact_email TEXT
          );
        """

        assert_postgres(postgres_schema, expected_schema)

    def test_openapi(self, openapi_schema: dict) -> None:
        """placeholder for OpenAPI assertions"""
        schemas = openapi_schema["components"]["schemas"]

        # Base/object schemas should be present
        assert "Company" in schemas
        assert "Address" in schemas
        assert "ContactInfo" in schemas

        company_props = schemas["Company"]["properties"]

        # API layer includes transient fields (transient only affects DB layer)
        assert "name" in company_props
        assert "headquarters" in company_props
        assert "office" in company_props
        assert "warehouse" in company_props

        # Address should reference ContactInfo
        address_props = schemas["Address"]["properties"]
        assert "contact" in address_props
        assert address_props["contact"]["$ref"] == "#/components/schemas/ContactInfo"

        # ContactInfo should expose both email and phone (phone is @transient -> DB-only)
        contact_props = schemas["ContactInfo"]["properties"]
        assert "email" in contact_props
        assert "phone" in contact_props

    def test_spring(self, srcgen: Path) -> None:
        """placeholder for Spring generation assertions"""
        src_root = srcgen / "src" / "main" / "java"
        assert src_root.exists()

        # Entity should exclude transient fields (DB layer)
        entity_files = list(src_root.rglob("CompanyEntity.java"))
        assert len(entity_files) == 1
        entity_content = entity_files[0].read_text(encoding="utf-8")

        # Flattened headquarters contact email should exist
        assert "headquartersContactEmail" in entity_content
        # Transient nested phone should NOT be part of the Entity (DB)
        assert "headquartersContactPhone" not in entity_content

        # Transient top-level fields (office, warehouse) should not be persisted as entity fields
        assert "office" not in entity_content
        assert "warehouse" not in entity_content

        # Request DTOs should include transient fields (API layer)
        request_files = list(src_root.rglob("CompanyRequest.java"))
        assert len(request_files) == 1
        request_content = request_files[0].read_text(encoding="utf-8")

        assert '@JsonProperty(value = "headquarters")' in request_content
        assert '@JsonProperty(value = "office")' in request_content
        assert '@JsonProperty(value = "warehouse")' in request_content

    @pytest.mark.integration
    def test_integration(self, srcgen: Path) -> None:
        """runs mvn clean test to verify generated code compiles and tests pass"""
        assert_mvn()
