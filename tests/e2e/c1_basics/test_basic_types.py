from pathlib import Path

import pytest

from tests.e2e import assert_mvn, assert_postgres
from tests.e2e.base_e2e_test import BaseE2ETest


class TestE2EBasicTypes(BaseE2ETest):
    """Test E2E for BASIC_TYPES testcase"""

    TESTCASE = """\
      base A {
        int: Int
        long: Long
      }

      base B extends A {
        float: Float
        double: Double
        string: String
      }

      type Foo extends B {
        boolean: Boolean
        date: Date
        datetime: Datetime
        object: Object
      }
    """

    def test_postgres(self, postgres_schema: str) -> None:
        """asserts generated Postgres schema is correct"""
        expected_schema = """\
          create table if not exists T_FOO (
            id BIGINT generated by default as identity primary key,
            uid VARCHAR unique,
            iv INTEGER,
            int INTEGER,
            long BIGINT,
            float REAL,
            double DOUBLE PRECISION,
            string TEXT,
            boolean BOOLEAN,
            date DATE,
            datetime TIMESTAMP,
            object JSONB
          );
        """

        assert_postgres(postgres_schema, expected_schema)

    def test_openapi(self, openapi_schema: dict) -> None:
        """asserts generated OpenAPI spec is correct"""

        schemas = openapi_schema["components"]["schemas"]

        foo = schemas["Foo"]
        foo_props = foo["properties"]

        assert foo_props["int"]["type"] == "integer"
        assert foo_props["int"]["format"] == "int32"

        assert foo_props["long"]["type"] == "integer"
        assert foo_props["long"]["format"] == "int64"

        assert foo_props["float"]["type"] == "number"
        assert foo_props["float"]["format"] == "float"

        assert foo_props["double"]["type"] == "number"
        assert foo_props["double"]["format"] == "double"

        assert foo_props["string"]["type"] == "string"
        assert foo_props["boolean"]["type"] == "boolean"

        assert foo_props["date"]["type"] == "string"
        assert foo_props["date"]["format"] == "date"

        assert foo_props["datetime"]["type"] == "string"
        assert foo_props["datetime"]["format"] == "date-time"

        assert foo_props["object"]["type"] == "object"

    def test_spring(self, srcgen: Path) -> None:
        """asserts generated Spring Boot code is correct"""

        pass

    @pytest.mark.integration
    def test_integration(self, srcgen: Path) -> None:
        """runs mvn clean test to verify generated code compiles and tests pass"""
        assert_mvn()
