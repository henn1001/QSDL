from pathlib import Path

import pytest

from tests.e2e import base_e2e_cases

from . import assert_schema
from .base_e2e_test import BaseE2ETest


class TestE2EOneToManyVariant1(BaseE2ETest):
    """Test E2E for ONE_TO_MANY[0] testcase"""

    TESTCASE = base_e2e_cases.ONE_TO_MANY[0]

    def test_postgres(self, postgres_schema: str) -> None:
        """asserts generated Postgres schema is correct"""
        expected_schema = """\
          create table if not exists T_USER (
            id BIGINT generated by default as identity primary key,
            uid VARCHAR unique,
            iv INTEGER
          );
          create table if not exists T_USER_METRIC_TO_T_METRIC (
            user_id BIGINT not null,
            metric_id BIGINT not null,
            primary key (user_id, metric_id)
          );
          create table if not exists T_METRIC (
            id BIGINT generated by default as identity primary key,
            uid VARCHAR unique,
            iv INTEGER,
            likes INTEGER
          );
          alter table if exists T_USER_METRIC_TO_T_METRIC add constraint FK_T_USER_METRIC_TO_T_METRIC_USER foreign key (user_id) references T_USER(id);
          alter table if exists T_USER_METRIC_TO_T_METRIC add constraint FK_T_USER_METRIC_TO_T_METRIC_METRIC foreign key (metric_id) references T_METRIC(id);
        """

        assert_schema(postgres_schema, expected_schema)

    def test_openapi(self, openapi_schema: dict) -> None:
        """asserts generated OpenAPI spec is correct"""

        pass

    def test_spring(self, srcgen: Path) -> None:
        """asserts generated Spring Boot code is correct"""

        pass

    @pytest.mark.integration
    def test_integration(self, srcgen: Path) -> None:
        """runs mvn clean test to verify generated code compiles and tests pass"""

        pass


