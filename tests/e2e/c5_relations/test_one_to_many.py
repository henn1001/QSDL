class TestE2EOneToMany(BaseE2ETest):
    """Test E2E for ONE_TO_MANY testcase"""

    TESTCASE = """\
        type User {
          primary_metric: [Metric]
          secondary_metric: [Metric]
        }
        type Metric {
          likes: Int
        }
    """

    def test_postgres(self, postgres_schema: str) -> None:
        """asserts generated Postgres schema is correct"""
        expected_schema = """\
            CREATE TABLE IF NOT EXISTS t_user (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER
            );

            CREATE TABLE IF NOT EXISTS t_metric (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              likes INTEGER,
              primary_metric_user_id BIGINT,
              secondary_metric_user_id BIGINT
            );

            ALTER TABLE IF EXISTS t_metric ADD CONSTRAINT fk_t_metric_primary_metric_user FOREIGN KEY (primary_metric_user_id) REFERENCES t_user(id);
            ALTER TABLE IF EXISTS t_metric ADD CONSTRAINT fk_t_metric_secondary_metric_user FOREIGN KEY (secondary_metric_user_id) REFERENCES t_user(id);
        """

        assert_postgres(postgres_schema, expected_schema)

