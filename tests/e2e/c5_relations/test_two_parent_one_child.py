from pathlib import Path

import pytest

from tests.e2e import assert_mvn, assert_postgres
from tests.e2e.base_e2e_test import BaseE2ETest


class TestE2ETwoParentOneChild(BaseE2ETest):
    """Test E2E for TWO_PARENT_ONE_CHILD testcase"""

    TESTCASE = """\
        type Fruit {
          name: String
        }
        type Foo {
          name: String
          basket: [Fruit] @composition
        }
        type Bar {
          name: String
          basket: [Fruit] @composition
        }
    """

    def test_postgres(self, postgres_schema: str) -> None:
        """asserts generated Postgres schema is correct"""
        expected_schema = """\
            CREATE TABLE IF NOT EXISTS t_fruit (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              name TEXT,
              basket_foo_id BIGINT,
              basket_bar_id BIGINT
            );

            CREATE TABLE IF NOT EXISTS t_foo (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              name TEXT
            );

            CREATE TABLE IF NOT EXISTS t_bar (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              name TEXT
            );

            ALTER TABLE IF EXISTS t_fruit ADD CONSTRAINT fk_t_fruit_basket_foo FOREIGN KEY (basket_foo_id) REFERENCES t_foo(id);
            ALTER TABLE IF EXISTS t_fruit ADD CONSTRAINT fk_t_fruit_basket_bar FOREIGN KEY (basket_bar_id) REFERENCES t_bar(id);
        """

        assert_postgres(postgres_schema, expected_schema)

