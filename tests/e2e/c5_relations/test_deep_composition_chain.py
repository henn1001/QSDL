from pathlib import Path

import pytest

from tests.e2e import assert_mvn, assert_postgres
from tests.e2e.base_e2e_test import BaseE2ETest


class TestE2EDeepCompositionChain(BaseE2ETest):
    """Test E2E for DEEP_COMPOSITION_CHAIN testcase"""

    TESTCASE = """\
      type Employee {
        name: String
        email: String
      }
      type Team {
        name: String
        members: [Employee] @composition
      }
      type Department {
        name: String
        teams: [Team] @composition
      }
      type Organization {
        name: String
        departments: [Department] @composition
      }
    """

    def test_postgres(self, postgres_schema: str) -> None:
        """asserts generated Postgres schema is correct"""
        expected_schema = """\
            CREATE TABLE IF NOT EXISTS t_employee (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              name TEXT,
              email TEXT,
              members_team_id BIGINT
            );

            CREATE TABLE IF NOT EXISTS t_team (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              name TEXT,
              teams_department_id BIGINT
            );

            CREATE TABLE IF NOT EXISTS t_department (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              name TEXT,
              departments_organization_id BIGINT
            );

            CREATE TABLE IF NOT EXISTS t_organization (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              name TEXT
            );

            ALTER TABLE IF EXISTS t_employee ADD CONSTRAINT fk_t_employee_members_team FOREIGN KEY (members_team_id) REFERENCES t_team(id);
            ALTER TABLE IF EXISTS t_team ADD CONSTRAINT fk_t_team_teams_department FOREIGN KEY (teams_department_id) REFERENCES t_department(id);
            ALTER TABLE IF EXISTS t_department ADD CONSTRAINT fk_t_department_departments_organization FOREIGN KEY (departments_organization_id) REFERENCES t_organization(id);
        """

        assert_postgres(postgres_schema, expected_schema)

