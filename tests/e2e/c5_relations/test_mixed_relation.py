from pathlib import Path

import pytest

from tests.e2e import assert_mvn, assert_postgres
from tests.e2e.base_e2e_test import BaseE2ETest


class TestE2EMixedRelation(BaseE2ETest):
    """Test E2E for MIXED_RELATION testcase"""

    TESTCASE = """\
      type Tag {
        name: String
      }
      type Category {
        name: String
      }
      type Product {
        name: String
        tags: [Tag] @composition
        categories: [Category] @aggregation
      }
    """

    def test_postgres(self, postgres_schema: str) -> None:
        """asserts generated Postgres schema is correct"""
        expected_schema = """\
            create table if not exists T_TAG (
              id BIGINT generated by default as identity primary key,
              uid VARCHAR unique,
              iv INTEGER,
              name TEXT,
              tags_product_id BIGINT
            );

            create table if not exists T_CATEGORY (
              id BIGINT generated by default as identity primary key,
              uid VARCHAR unique,
              iv INTEGER,
              name TEXT
            );

            create table if not exists T_PRODUCT (
              id BIGINT generated by default as identity primary key,
              uid VARCHAR unique,
              iv INTEGER,
              name TEXT
            );

            create table if not exists T_PRODUCT_TO_T_CATEGORY (
              product_id BIGINT not null,
              category_id BIGINT not null,
              primary key (product_id, category_id)
            );

            alter table if exists T_TAG add constraint FK_T_TAG_TAGS_PRODUCT foreign key (tags_product_id) references T_PRODUCT(id);
            alter table if exists T_PRODUCT_TO_T_CATEGORY add constraint FK_T_PRODUCT_TO_T_CATEGORY_PRODUCT foreign key (product_id) references T_PRODUCT(id);
            alter table if exists T_PRODUCT_TO_T_CATEGORY add constraint FK_T_PRODUCT_TO_T_CATEGORY_CATEGORY foreign key (category_id) references T_CATEGORY(id);
        """

        assert_postgres(postgres_schema, expected_schema)

