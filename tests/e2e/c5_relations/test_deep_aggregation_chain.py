from pathlib import Path

import pytest

from tests.e2e import assert_mvn, assert_postgres
from tests.e2e.base_e2e_test import BaseE2ETest


class TestE2EDeepAggregationChain(BaseE2ETest):
    """Test E2E for DEEP_AGGREGATION_CHAIN testcase"""

    TESTCASE = """\
      type Address {
        street: String
        city: String
      }
      type Student {
        name: String
        addresses: [Address] @aggregation
      }
      type Course {
        name: String
        students: [Student] @aggregation
      }
      type University {
        name: String
        courses: [Course] @aggregation
      }
    """

    def test_postgres(self, postgres_schema: str) -> None:
        """asserts generated Postgres schema is correct"""
        expected_schema = """\
            CREATE TABLE IF NOT EXISTS t_address (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              street TEXT,
              city TEXT
            );

            CREATE TABLE IF NOT EXISTS t_student (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              name TEXT
            );

            CREATE TABLE IF NOT EXISTS t_student_to_t_address (
              student_id BIGINT NOT NULL,
              address_id BIGINT NOT NULL,
              PRIMARY KEY (student_id, address_id)
            );

            CREATE TABLE IF NOT EXISTS t_course (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              name TEXT
            );

            CREATE TABLE IF NOT EXISTS t_course_to_t_student (
              course_id BIGINT NOT NULL,
              student_id BIGINT NOT NULL,
              PRIMARY KEY (course_id, student_id)
            );

            CREATE TABLE IF NOT EXISTS t_university (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              uid VARCHAR UNIQUE,
              iv INTEGER,
              name TEXT
            );

            CREATE TABLE IF NOT EXISTS t_university_to_t_course (
              university_id BIGINT NOT NULL,
              course_id BIGINT NOT NULL,
              PRIMARY KEY (university_id, course_id)
            );

            ALTER TABLE IF EXISTS t_student_to_t_address ADD CONSTRAINT fk_t_student_to_t_address_student FOREIGN KEY (student_id) REFERENCES t_student(id);
            ALTER TABLE IF EXISTS t_student_to_t_address ADD CONSTRAINT fk_t_student_to_t_address_address FOREIGN KEY (address_id) REFERENCES t_address(id);
            ALTER TABLE IF EXISTS t_course_to_t_student ADD CONSTRAINT fk_t_course_to_t_student_course FOREIGN KEY (course_id) REFERENCES t_course(id);
            ALTER TABLE IF EXISTS t_course_to_t_student ADD CONSTRAINT fk_t_course_to_t_student_student FOREIGN KEY (student_id) REFERENCES t_student(id);
            ALTER TABLE IF EXISTS t_university_to_t_course ADD CONSTRAINT fk_t_university_to_t_course_university FOREIGN KEY (university_id) REFERENCES t_university(id);
            ALTER TABLE IF EXISTS t_university_to_t_course ADD CONSTRAINT fk_t_university_to_t_course_course FOREIGN KEY (course_id) REFERENCES t_course(id);
        """

        assert_postgres(postgres_schema, expected_schema)

