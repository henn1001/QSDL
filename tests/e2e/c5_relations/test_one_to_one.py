from pathlib import Path

import pytest

from tests.e2e import assert_mvn, assert_postgres
from tests.e2e.base_e2e_test import BaseE2ETest


class TestE2EOneToOne(BaseE2ETest):
    """Test E2E for ONE_TO_ONE testcase

    one-to-one relations are modelled as unique foreign keys  in the owning entity's table.

    * assert that foreign keys are unique
    * assert that foreign keys are in the owning entity's table
    * assert that we can use the same entity type more than once in a one-to-one relation
    * assert at least two levels of nesting works

    """

    TESTCASE = """\
      type User {
        primary_metric: Metric
        secondary_metric: Metric
      }
      type Metric {
        likes: Int
        role: Role
      }
      type Role {
        name: String
        foobar: Unrelated
      }
      type Unrelated {
        description: String
      }
    """

    def test_postgres(self, postgres_schema: str) -> None:
        """asserts generated Postgres schema is correct"""
        expected_schema = """\
            create table if not exists T_USER (
              id BIGINT generated by default as identity primary key,
              uid VARCHAR unique,
              iv INTEGER,
              primary_metric_metric_id BIGINT,
              secondary_metric_metric_id BIGINT
            );

            create table if not exists T_METRIC (
              id BIGINT generated by default as identity primary key,
              uid VARCHAR unique,
              iv INTEGER,
              likes INTEGER,
              role_role_id BIGINT
            );

            create table if not exists T_ROLE (
              id BIGINT generated by default as identity primary key,
              uid VARCHAR unique,
              iv INTEGER,
              name TEXT,
              foobar_unrelated_id BIGINT
            );

            create table if not exists T_UNRELATED (
              id BIGINT generated by default as identity primary key,
              uid VARCHAR unique,
              iv INTEGER,
              description TEXT
            );

            alter table if exists T_USER add constraint FK_T_USER_PRIMARY_METRIC foreign key (primary_metric_metric_id) references T_METRIC(id);
            alter table if exists T_USER add constraint FK_T_USER_SECONDARY_METRIC foreign key (secondary_metric_metric_id) references T_METRIC(id);
            alter table if exists T_METRIC add constraint FK_T_METRIC_ROLE foreign key (role_role_id) references T_ROLE(id);
            alter table if exists T_ROLE add constraint FK_T_ROLE_FOOBAR foreign key (foobar_unrelated_id) references T_UNRELATED(id);
        """

        assert_postgres(postgres_schema, expected_schema)

